package com.example.tugas3;

public class Model {
    private String header;
    private String lirik;
    private int gambar;
    private int btn;

    public String getHeader() {
        return header;
    }

    public void setHeader(String header) {
        this.header = header;
    }

    public String getDesc() {
        return lirik;
    }

    public void setDesc(String lirik) {
        this.lirik = lirik;
    }

    public int getImgname() {
        return gambar;
    }

    public void setImgname(int gambar) {
        this.gambar = gambar;
    }

    public int getBtn() {
        return btn;
    }

    public void setBtn(int btn) {
        this.btn = btn;
    }
}



